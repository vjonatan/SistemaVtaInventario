    private void limpiarTabla(){
        int numeroFilas = modeloTabla.getRowCount();
        if (numeroFilas > 0) {
            for (int i = numeroFilas - 1; i >= 0; i--) {
                modeloTabla.removeRow(i);
            }    
        }        
    }
    

